// absolute number
var number = -5;
var absoluteNumber = Math.abs(number);
console.log(absoluteNumber);

// number round
var number = 5.56839;
var result = Math.round(number);
console.log(result);

//ceiling
var number = 8.06839;
var result = Math.ceil(number);
console.log(result);

//flooring

var number = 10.96839;
var result = Math.floor(number);
console.log(result);

//random value
var number = Math.random()*100;
var result = Math.round(number);
console.log(result);